# Type of 10 - INT
print(type(10))

# Type of 3.1415 - FLOAT
print(type(3.1415))

# Type of "Hello!!!" -  STR
print(type("Hello!!!"))

# Type of True - BOOL
print(type(True))

# Convert True to INT --> (1)
print(int(True))

# Convert False to INT --> (0)
print(int(False))

# Convert 1 to boolean - True
print(bool(1))

# Convert 0 to boolean - False
print(bool(0))
